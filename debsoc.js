var white_box1 = document.getElementById("event1");
var text1 = document.getElementById("collapse-text1");
var img1 = document.getElementById("collapsible1");
img1.addEventListener("click", collapse1);
function collapse1(){
    if(text1.style.display === "none"){
        text1.style.display = "grid"
        white_box1.style.width = "50%";
    }
    else{
        text1.style.display = "none"
        white_box1.style.width = "14%"
    }
}

